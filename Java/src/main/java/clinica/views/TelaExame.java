    private void JBSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBSalvarActionPerformed
        try{
            if(!"".equals(JFTNumTestes.getText()))
            {
                int numeroTeste = Integer.parseInt(JFTNumTestes.getText());
                
                String resultado = (String) JCResultado.getSelectedItem();                
                EResultado eResultado = EResultado.valueOf(resultado);
                
                int idx = JBRTecnico.getSelectedIndex();                
                Usuario responsaTecnico = responsavelTecnicos.get(idx);
                
                int idx2 = JCColeta.getSelectedIndex();                
                Coleta coleta = coletas.get(idx2);
  
                Exame exame;
                
                ExameDAO exameDAO = new ExameDAO();
                
                if(operacao == null){
                    exame = new Exame(0, responsaTecnico, coleta.getPaciente(), coleta, numeroTeste, eResultado);
                    exameDAO.salvar(exame);
                }
                else if(operacao.equals("Editar")){
                    exame = new Exame(exameGlobal.getId(), responsaTecnico, coleta.getPaciente(), coleta, numeroTeste, eResultado);
                    exameDAO.alterar(exame);
                }
                
                JOptionPane.showMessageDialog(null, "Opera√ß√£o realizada com Sucesso!!!");
                TelaLogin tela = new TelaLogin();
                tela.setVisible(true);
                this.setVisible(false);
            }
            else {
                JOptionPane.showMessageDialog(null, "Campo numero de teste √© obrigat√≥rio.");
            }  
        }
        catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_JBSalvarActionPerformed

    String[] dados = {"INCONCLUSIVO", "INVALIDO", "DETECTADO", "RECOLETAFORAPRAZO", "RECOLETAPOUCOMATERIAL", 
    "NAODETECTADO"};
    
    ArrayList<Coleta> coletas;
    ArrayList<Usuario> responsavelTecnicos;
    
    private void preencheCombo() throws SQLException, ClassNotFoundException {        
        
        JCResultado.removeAllItems(); 

        for(int i = 0; i < dados.length; i++) {
            JCResultado.addItem(dados[i]);
        }
        
        coletas = new ColetaDAO().buscarTodos();
        JCColeta.removeAllItems();
        
        for(int i = 0; i < coletas.size(); i++){
            JCColeta.addItem(coletas.get(i).getId() + " - " + coletas.get(i).getPaciente().getNome());  
        }
        
        responsavelTecnicos = new UsuarioDAO().buscarResponsaveisTecnicos();
        JBRTecnico.removeAllItems();
        
        for(int i = 0; i < responsavelTecnicos.size(); i++){
            JBRTecnico.addItem(responsavelTecnicos.get(i).getNome());  
        }
    }

    

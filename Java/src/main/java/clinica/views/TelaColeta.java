    private void JBSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBSalvarActionPerformed
        try{
                int idx = JCColetor.getSelectedIndex();                
                Usuario coletor = coletores.get(idx);
                
                int idx2 = JCPaciente.getSelectedIndex();                
                Usuario paciente = pacientes.get(idx2);
                
                Boolean idade = false;
                if(JRAte60.isSelected()){
                    idade = true;
                }
                else if(JRMais60.isSelected()){
                    idade = false;
                }
                
                String materialColetado = (String) JBMColetado.getSelectedItem();                
                EMaterialColetado eMaterialColetado = EMaterialColetado.valueOf(materialColetado);
                
                Boolean comorbidade = false;
                if(JRSim.isSelected()){
                    comorbidade = true;
                }
                else if(JRNao.isSelected()){
                    comorbidade = false;
                }
  
                Coleta coleta;
                
                ColetaDAO coletaDAO = new ColetaDAO();
                
                if(operacao == null){
                    coleta = new Coleta(0, coletor, paciente, idade, eMaterialColetado, comorbidade);
                    coletaDAO.salvar(coleta);
                }
                else if(operacao.equals("Editar")){
                    coleta = new Coleta(coletaGlobal.getId(), coletor, paciente, idade, eMaterialColetado, comorbidade);
                    coletaDAO.alterar(coleta);
                }
                
                JOptionPane.showMessageDialog(null, "Opera√ß√£o realizada com Sucesso!!!");
                TelaLogin tela = new TelaLogin();
                tela.setVisible(true);
                this.setVisible(false);             
        }
        catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_JBSalvarActionPerformed
    
    String[] dados = {"ASPIRADOTRAQUEAL", "ESCARRO", "LAVADOBRONCOALVEOLAR", "ASPIRADOBRONQUICO", 
    "ASPIRADONASOFARINGEO", "SWABCOMBINADODEOROFARINGEENASOFARINGE",
    "SECRECAOTRAQUEAL", "SWABDENASOFARINGE"};
    
    ArrayList<Usuario> pacientes;
    ArrayList<Usuario> coletores;
    
    private void preencheCombo() throws SQLException, ClassNotFoundException {        
        
        JBMColetado.removeAllItems(); 

        for(int i = 0; i < dados.length; i++) {
            JBMColetado.addItem(dados[i]);
        }
        
        pacientes = new UsuarioDAO().buscarPacientes();
        JCPaciente.removeAllItems();
        
        for(int i = 0; i < pacientes.size(); i++){
            JCPaciente.addItem(pacientes.get(i).getNome());
        }
        
        coletores = new UsuarioDAO().buscarCadastros();
        JCColetor.removeAllItems();
        
        for(int i = 0; i < coletores.size(); i++){
            JCColetor.addItem(coletores.get(i).getNome());
        }
    }
    
